public class SalaryPercentage {
public static void main(String[] args){
    float salary = 5000.00F;
    float result = salary * 0.3F;
    System.out.println("30% de 5000.00 eh: " + result );
    result = salary * 0.15F;
    System.out.println("15% de 5000.00 eh: " + result);
    result = salary * 0.05F;
    System.out.println("5% de 5000.00 eh: " + result);
}}
